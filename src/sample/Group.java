package sample;

/**
 *
 * @author Maggie
 */
public class Group {
    private String name; //组别名称
    private String color; //组别代表色

    public Group(String name, String color) {
        this.name = name;
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setColor(String color) {
        this.color = color;
    }
    
    
}
